num_str = "0123456789"

# 截取从2-5的字符串
print(num_str[2: 6])

# 截取从2 ~末尾的字符串
print(num_str[2:])

# 截取从开始 ~5的字符串
print(num_str[:6])

# 截取完成整的字符串
print(num_str)

# 从开始位置，每个一个字符串截取字符串
print(num_str[::2])

# 从索引1开始，每隔一个字符串取一个
print(num_str[1::2])

# 截取从2 ~末尾 -1的字符串
print(num_str[2:-1])

# 截取末尾两个字符串 !!!!!
print(num_str[-2:])

# 字符串的逆序  步长为-1时，从右往左切
print(num_str[::-1])






